DANGEROUS_INTENT_KEYWORDS = (
    "配方",
    "比例",
    "制备",
    "合成",
    "点火",
    "引爆",
    "爆炸",
    "威力",
    "燃烧更强",
    "提高能量",
    "放大",
    "武器",
    "破坏",
)


def has_dangerous_intent(text: str) -> bool:
    normalized = (text or "").lower()
    return any(keyword in normalized for keyword in DANGEROUS_INTENT_KEYWORDS)


def safe_refusal_prefix() -> str:
    return (
        "我不能提供危险配方、具体制备比例、制备/点火步骤，或任何提升爆炸/燃烧威力的指导。"
        "下面仅基于已上传数据做安全的科研趋势分析、图像质量评估与复测候选排序。"
    )
